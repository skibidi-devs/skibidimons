bool32 ExpansionMonToVanillaMon(struct Pokemon *src, struct VanillaPokemon *dst)
{
    u32 i, ball, item, vanMovesId;
    u32 speciesExpansion = GetMonData(src, MON_DATA_SPECIES_OR_EGG);
    u32 level = GetMonData(src, MON_DATA_LEVEL);
    u32 personality = GetMonData(src, MON_DATA_PERSONALITY);
    u32 otId = GetMonData(src, MON_DATA_OT_ID);

    s32 vanillaSpecies = SpeciesExpansionToVanilla(speciesExpansion);
    if (vanillaSpecies == -1)
        return FALSE;

    Vanilla_CreateMon(dst, vanillaSpecies, level, personality, otId);
    CopyAllMonData(src, dst, TRUE);

    ball = ItemExpansionToVanilla(GetMonData(src, MON_DATA_POKEBALL));
    if (ball == -1) // Not a gen3 ball
        ball = VANILLA_ITEM_POKE_BALL;
    Vanilla_SetMonData(dst, VANILLA_MON_DATA_POKEBALL, &ball);

    item = ItemExpansionToVanilla(GetMonData(src, MON_DATA_HELD_ITEM));
    if (item == -1) // Not a gen3 item
        item = VANILLA_ITEM_NONE;
    Vanilla_SetMonData(dst, VANILLA_MON_DATA_HELD_ITEM, &item);

    // copy only gen3 moves
    for (vanMovesId = 0, i = 0; i < MAX_MON_MOVES; i++)
    {
        u32 move = GetMonData(src, MON_DATA_MOVE1 + i);
        if (move < MOVES_COUNT_GEN3)
        {
            u32 pp = GetMonData(src, MON_DATA_PP1 + i);
            Vanilla_SetMonData(dst, VANILLA_MON_DATA_MOVE1 + vanMovesId, &move);
            Vanilla_SetMonData(dst, VANILLA_MON_DATA_PP1 + vanMovesId, &pp);
            vanMovesId++;
        }
    }

    return TRUE;
}

bool32 VanillaMonToExpansion(struct VanillaPokemon *src, struct Pokemon *dst)
{
    u32 i, ball, item;
    u32 vanillaSpecies = Vanilla_GetMonData(src, VANILLA_MON_DATA_SPECIES_OR_EGG, NULL);
    u32 level = Vanilla_GetMonData(src, VANILLA_MON_DATA_LEVEL, NULL);
    u32 personality = Vanilla_GetMonData(src, VANILLA_MON_DATA_PERSONALITY, NULL);
    u32 otId = Vanilla_GetMonData(src, VANILLA_MON_DATA_OT_ID, NULL);

    s32 speciesExpansion = SpeciesVanillaToExpansion(vanillaSpecies);
    if (speciesExpansion == -1)
        return FALSE;

    CreateMon(dst, speciesExpansion, level, 0, TRUE, personality, OT_ID_PRESET, otId);
    CopyAllMonData(dst, src, FALSE);

    ball = ItemVanillaToExpansion(Vanilla_GetMonData(src, VANILLA_MON_DATA_POKEBALL, NULL));
    if (ball == -1) // Somehow not a valid ball
        ball = VANILLA_ITEM_POKE_BALL;
    SetMonData(dst, MON_DATA_POKEBALL, &ball);

    item = ItemVanillaToExpansion(Vanilla_GetMonData(src, VANILLA_MON_DATA_HELD_ITEM, NULL));
    if (item == -1) // Somehow not a valid item
        item = ITEM_NONE;
    SetMonData(dst, MON_DATA_HELD_ITEM, &item);

    // all vanilla moves are the same as in expansion
    for (i = 0; i < MAX_MON_MOVES; i++)
    {
        CopyMonData(dst, src, MON_DATA_MOVE1 + i, VANILLA_MON_DATA_MOVE1 + i, FALSE);
        CopyMonData(dst, src, MON_DATA_PP1 + i, VANILLA_MON_DATA_PP1 + i, FALSE);
    }

    return TRUE;
}

// Two at once.
void SendExpansionMonsToVanilla(struct Pokemon *src, u32 mode)
{
    struct VanillaPokemon vanillaMons[2];

    ExpansionMonToVanillaMon(&src[0], &vanillaMons[0]);
    ExpansionMonToVanillaMon(&src[1], &vanillaMons[1]);

    if (mode == SEND_TRADE)
        memcpy(gBlockSendBuffer, vanillaMons, 2 * sizeof(struct VanillaPokemon));
    else if (mode == SEND_BATTLE)
        SendBlock(BitmaskAllOtherLinkPlayers(), vanillaMons, 2 * sizeof(struct VanillaPokemon));
}

// Two at once.
void ReceiveVanillaMons(struct Pokemon *dst, void *buffer, u32 mode)
{
    struct VanillaPokemon vanillaMons[2];

    if (mode == SEND_TRADE || mode == SEND_BATTLE)
        memcpy(vanillaMons, buffer, 2 * sizeof(struct VanillaPokemon));

    VanillaMonToExpansion(&vanillaMons[0], &dst[0]);
    VanillaMonToExpansion(&vanillaMons[1], &dst[1]);
}

